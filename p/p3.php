  <h5>Overview:</h5>
<p>The ERP system is a comprehensive enterprise resource planning solution designed to streamline and automate business operations across multiple departments, including human resources, finance, project management, and inventory management. It offers a centralized platform for managing workflows, optimizing resources, and generating insightful reports.</p>

<h5>Key Features:</h5>
<ul>
  <li><strong>User Management:</strong> Centralized management of user accounts with customizable access levels.</li>
  <li><strong>Role Management:</strong> Assign and manage user roles and permissions.</li>
  <li><strong>Human Resource Management:</strong> Manage employee records, work schedules, and HR-related tasks.</li>
  <li><strong>Finance Management:</strong> Manage various financial operations like Bid Bond, Pay Orders, Performance Guarantees, and Advanced Performance Guarantees.</li>
  <li><strong>Projects Management:</strong> Track project status, deadlines, and progress.</li>
  <li><strong>Marketing:</strong> Handle marketing campaigns, resources, and customer relationships.</li>
  <li><strong>Work Order Management:</strong> Generate and manage work orders for various departments and tasks.</li>
  <li><strong>Composite Limit:</strong> Manage loan types, limits, and track loan-related transactions, including receiving, refunds, and advances.</li>
  <li><strong>Cheque Management:</strong> Handle all cheque operations, including cheque book issuance, cheque issue, and cheque types (e.g., security, post-dated, withdrawal).</li>
  <li><strong>Bank Guarantee & Loan Management:</strong> Manage bank guarantees, W/O loan transactions, and related reports.</li>
  <li><strong>Reports:</strong> Detailed reports for cashbook, bankbook, ledger, balance sheet, trial balance, and income statement.</li>
  <li><strong>Intercompany Transactions:</strong> Handle approvals, voucher verification, and audit observations across different departments and companies.</li>
  <li><strong>ALVEN AIS:</strong> A dedicated dashboard to manage account heads, chart of accounts, clients, suppliers, and party members.</li>
  <li><strong>Receivables & Payables Management:</strong> Track sales, purchase vouchers, payments, and dues.</li>
</ul>

<h5>Challenges Addressed:</h5>
<ul>
  <li>Integrated a wide range of operations into a single platform to reduce administrative overhead.</li>
  <li>Developed robust role-based access control for secure data handling across departments.</li>
  <li>Implemented complex financial management features to track loans, payments, and guarantees efficiently.</li>
  <li>Enabled automated reporting to simplify decision-making and maintain transparency.</li>
</ul>

<h5>Achievements:</h5>
